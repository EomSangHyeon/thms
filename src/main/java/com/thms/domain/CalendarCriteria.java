package com.thms.domain;

public class CalendarCriteria {
	private int year;
	private int month;

	//Getter
	public int getYear() {
		return year;
	}
	public int getMonth() {
		return month;
	}

	//Setter
	public void setYear(int year) {
		this.year = year;
	}
	public void setMonth(int month) {
		this.month = month;
	}
}
